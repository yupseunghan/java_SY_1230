/*
요구사항 명세서
회원관리
	-회원가입
		-사용자는 아이디, 비번, 이메일 전화번호를 입력하여 회원 가입을 한다
        -아이디는 영문자, 숫자만 가능하며 6~13자이다
        -비번은 영문자, 숫자, 특수문자(!@#$%)만 가능하며 6~20자이다
        -이메일은 이메일 형식에 맞게 작성한다
        -전화번호는 전화번호 형식에 맞아야 한다
			-000-0000-0000
		-아이디,비번,이메일,전화번호는 필수 항목이다
        -아이디,이메일,전화번호는 중복되면 안 된다
	-로그인
		-사용자는 아이디,비번을 입력하여 로그인한다
        -아이디, 비번이 일치하면 로그인에 성공한다.
        -아이디, 비번 중 하나라도 일치하지 않으면 로그인에 실패한다.
        -로그인에 실패하면 실패 횟수가 증가하고, 로그인 실패 횟수가 5회가 되면 로그인이 불가능하다.
        -로그인 성공 시 실패 횟수가 0으로 초기화 된다.
	-로그아웃
		-회원은 로그아웃을 클릭하면 로그아웃 한다.
	-비번찾기
		-회원은 가입한 아이디와 이메일을 입력하여 비번을 찾는다
        -입력한 아이디, 이메일과 일치하는 회원이 있다면 이메일로 새 비번을 발급한다.
        -입력한 아이디, 이메일과 일치하는 회원이 없다면 알림 메세지를 띄운다.
			-일치하는 정보가 없습니다.
	-회원탈퇴
		-회원은 회원탈퇴를 한다.
        -회원 탈퇴 시 아이디는 삭제되지 않으며, 비번, 이메일, 전화번호가 삭제 된다.
제품관리
	-분류 등록
		-관리자는 분류명 최대 10자, 분류 코드를 입력하여 제품의 분류를 등록한다
        -분류 코드는 영어 대문자 3자리이다.
        -분류는 대분류만 있다.
	-분류 수정
		-관리자는 기존 분류명을 새 분류명, 새 분류 코드를 입력하여 수정한다.
        -입력하는 새 분류명, 새 분류코드 중 하나라도 중복된 경우 알림 메세지를 띄운다.
			-이미 존재하는 분류입니다.
	-분류 삭제
		-관리자는 삭제할 분류를 선택하여 분류를 삭제한다.
        -이 때 분류에 제품이 등록된 경우, 분류를 삭제하지 않는다.
	-제품 등록
		-관리자는 제품 코드, 제품명, 분류, 상세 내용, 가격, 썸네일을 입력하여 제품을 등록한다.
        -제품 코드는 분류코드3자리 ,제품순서 3자리를 합친 문자열로, 자동으로 발급되고 중복X.
        -제품 등록 순서에서 3자리가 안되는 경우 앞에 0으로 채운다.
        -제품명은 최대 100자이다.
        -상세 내용은 제한이 없다
        -가격은 0원 이상이다.
        -썸네일은 이미지 최대 1장이고, 없는 경우는 이미지 없음 이미지를 화면에 출력한다
	-제품 수정
		-관리자는 제품 정보를 수정한다.
        -수정할 수 있는 내용은 제품명, 상세 내용,가격,썸네일이다.
        -썸네일을 선택하지 않으면 기존 썸네일을 이용한다.
	-제품 삭제
		-관리자는 제품을 선택하여 삭제한다.
        -삭제할 떄 실제 제품 정보를 삭제하진 않는다.
	-제품 조회
		-사용자는 분류를 선택하여 제품을 조회한다.
        -사용자는 검색을 통해 검색어가 제품과 상세 내용에 포함된 제품명을 조회한다.
	-장바구니 추가
		-회원은 제품과 수량을 선택하여 장바구니에 담는다.
        -이미 장바구니에 있는 제품인 경우 수량만 변경된다.
	-장바구니 수정
		-회원은 장바구니에 담긴 제품을 선택하여 수량을 수정한다.
	-장바구니 삭제
		-회원은 장바구니에 담긴 제품을 선택하여 삭제한다.
	-제품 구매
		-회원은 제품 상세 조회에서 제품의 수량을 선택해서 제품을 바로 구매한다.
        -회원은 장바구니에서 제품과 수량을 선택해서 여러 제품을 한번에 구매한다.
	-구매 취소
		-회원은 구매 제품중 배송 전인 제품에 한에 구매를 취소한다.
	-제품 환불/교환
		-회원은 제품의 문제가 있으면 제품을 환불 또는 교환 한다.
	-구매 확정
		-회원은 배송된 제품의 문제가 없으면 구매를 확정한다.
        -구매 확정 시 환불/교환이 불가능하다
        
*/